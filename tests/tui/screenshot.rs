//! Terminal screenshot renderer — converts captured terminal state to PNG.
//!
//! Renders each character cell as a colored block with an embedded 8×16
//! bitmap font for readable text. The resulting image looks like a real
//! terminal screenshot.
//!
//! Usage:
//! ```no_run
//! let capture = ScreenCapture::from_pty(&harness);
//! let img = render_screenshot(&capture);
//! save_screenshot("startup", &img);
//! ```

use image::Rgba;
use image::RgbaImage;

use super::snapshot::ScreenCapture;

/// Cell dimensions in pixels (classic VGA 8×16).
const CELL_W: u32 = 8;
const CELL_H: u32 = 16;

/// Padding around the terminal content.
const PADDING: u32 = 8;

/// Render a terminal screen capture to a PNG-compatible RGBA image.
pub fn render_screenshot(capture: &ScreenCapture) -> RgbaImage {
    let img_w = u32::from(capture.cols) * CELL_W + PADDING * 2;
    let img_h = u32::from(capture.rows) * CELL_H + PADDING * 2;

    let mut img = RgbaImage::from_pixel(img_w, img_h, Rgba([0x1a, 0x1a, 0x2e, 0xff]));

    for (row_idx, row) in capture.cells.iter().enumerate() {
        for (col_idx, cell) in row.iter().enumerate() {
            let x0 = PADDING + (col_idx as u32) * CELL_W;
            let y0 = PADDING + (row_idx as u32) * CELL_H;

            // Background
            let bg = if cell.inverse {
                cell.fg.to_fg_rgba()
            } else {
                cell.bg.to_rgba()
            };

            for dy in 0..CELL_H {
                for dx in 0..CELL_W {
                    img.put_pixel(x0 + dx, y0 + dy, Rgba(bg));
                }
            }

            // Foreground character
            let ch = if cell.text.is_empty() {
                ' '
            } else {
                cell.text.chars().next().unwrap_or(' ')
            };

            if ch != ' ' {
                let fg = if cell.inverse {
                    cell.bg.to_rgba()
                } else {
                    cell.fg.to_fg_rgba()
                };

                // Render character glyph
                if let Some(glyph) = get_glyph(ch) {
                    for (dy, &row_bits) in glyph.iter().enumerate() {
                        for dx in 0..8u32 {
                            if row_bits & (0x80 >> dx) != 0 {
                                let px = x0 + dx;
                                let py = y0 + dy as u32;
                                if px < img_w && py < img_h {
                                    img.put_pixel(px, py, Rgba(fg));
                                }
                            }
                        }
                    }
                } else {
                    // Unknown character — render as a small filled block
                    for dy in 2..14 {
                        for dx in 1..7 {
                            let px = x0 + dx;
                            let py = y0 + dy;
                            if px < img_w && py < img_h {
                                img.put_pixel(px, py, Rgba(fg));
                            }
                        }
                    }
                }

                // Bold: shift right by 1px (classic VGA bold effect)
                if cell.bold && let Some(glyph) = get_glyph(ch) {
                    for (dy, &row_bits) in glyph.iter().enumerate() {
                        for dx in 0..7u32 {
                            if row_bits & (0x80 >> dx) != 0 {
                                let px = x0 + dx + 1;
                                let py = y0 + dy as u32;
                                if px < img_w && py < img_h {
                                    img.put_pixel(px, py, Rgba(fg));
                                }
                            }
                        }
                    }
                }

                // Underline: draw a line at row 14
                if cell.underline {
                    for dx in 0..CELL_W {
                        let px = x0 + dx;
                        let py = y0 + 14;
                        if px < img_w && py < img_h {
                            img.put_pixel(px, py, Rgba(fg));
                        }
                    }
                }
            }
        }
    }

    img
}

/// Save a screenshot PNG to the captures directory.
pub fn save_screenshot(name: &str, img: &RgbaImage) {
    let dir = std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("tests").join("tui").join("captures");
    std::fs::create_dir_all(&dir).ok();
    let path = dir.join(format!("{name}.png"));
    img.save(&path).expect("failed to save screenshot PNG");
}

/// Save a screenshot from a screen capture in one step.
pub fn capture_and_save_screenshot(name: &str, capture: &ScreenCapture) {
    let img = render_screenshot(capture);
    save_screenshot(name, &img);
}

// ── Embedded 8×16 bitmap font (ASCII 32–126) ────────────────
//
// Each character is 16 rows of 8 bits. Bit 7 (0x80) is leftmost pixel.
// Based on the classic IBM VGA/CGA font (public domain, circa 1981).

fn get_glyph(ch: char) -> Option<&'static [u8; 16]> {
    let idx = ch as u32;
    if (32..=126).contains(&idx) {
        Some(&FONT_8X16[(idx - 32) as usize])
    } else {
        // Box-drawing and other special characters
        match ch {
            '─' => Some(&GLYPH_HORIZ),
            '│' => Some(&GLYPH_VERT),
            '┌' => Some(&GLYPH_TOP_LEFT),
            '┐' => Some(&GLYPH_TOP_RIGHT),
            '└' => Some(&GLYPH_BOT_LEFT),
            '┘' => Some(&GLYPH_BOT_RIGHT),
            '├' => Some(&GLYPH_T_RIGHT),
            '┤' => Some(&GLYPH_T_LEFT),
            '┬' => Some(&GLYPH_T_DOWN),
            '┴' => Some(&GLYPH_T_UP),
            '┼' => Some(&GLYPH_CROSS),
            '▸' | '►' => Some(&GLYPH_TRIANGLE_RIGHT),
            '●' | '•' => Some(&GLYPH_BULLET),
            '█' => Some(&GLYPH_FULL_BLOCK),
            _ => None,
        }
    }
}

